class MetaDataFeatures:   
    
    
    def getID(self):
        return self.sid

    def setID(self, sid):
        self.sid = sid

    def getDate(self):
        return self.date

    def setDate(self, date):
        self.date = date

    def getText(self):
        return self.text

    def setText(self, text):
        self.text = text

    def getLabel(self):
        return self.label

    def setLabel(self, label):
        self.label = label

    
